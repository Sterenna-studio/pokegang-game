-- Pokegang Game / Pokeforge Supabase schema
-- Run this in the Supabase SQL Editor for the project used by config.js.
--
-- This file covers every table currently used by:
-- - modules/systems/cloudAccount.js
-- - modules/systems/gangCompetition.js
--
-- It is safe to rerun for normal updates: policies are recreated and missing
-- columns are added. If an existing table has a different primary key shape,
-- fix that manually before relying on upserts.

-- Optional UUID helper. Supabase projects usually already provide gen_random_uuid().
create extension if not exists pgcrypto;

-- ============================================================================
-- Cloud save: one current save per authenticated user and local save slot.
-- ============================================================================

create table if not exists public.pokegang_saves (
  user_id  uuid        not null references auth.users(id) on delete cascade,
  slot     integer     not null default 0,
  state    jsonb       not null,
  saved_at timestamptz not null default now(),
  primary key (user_id, slot)
);

alter table public.pokegang_saves add column if not exists user_id uuid references auth.users(id) on delete cascade;
alter table public.pokegang_saves add column if not exists slot integer not null default 0;
alter table public.pokegang_saves add column if not exists state jsonb not null default '{}'::jsonb;
alter table public.pokegang_saves add column if not exists saved_at timestamptz not null default now();

alter table public.pokegang_saves enable row level security;

drop policy if exists "player_saves_select_own" on public.pokegang_saves;
drop policy if exists "player_saves_insert_own" on public.pokegang_saves;
drop policy if exists "player_saves_update_own" on public.pokegang_saves;
drop policy if exists "player_saves_delete_own" on public.pokegang_saves;

create policy "player_saves_select_own"
  on public.pokegang_saves for select
  to authenticated
  using ((select auth.uid()) = user_id);

create policy "player_saves_insert_own"
  on public.pokegang_saves for insert
  to authenticated
  with check ((select auth.uid()) = user_id);

create policy "player_saves_update_own"
  on public.pokegang_saves for update
  to authenticated
  using ((select auth.uid()) = user_id)
  with check ((select auth.uid()) = user_id);

create policy "player_saves_delete_own"
  on public.pokegang_saves for delete
  to authenticated
  using ((select auth.uid()) = user_id);

create index if not exists player_saves_user_slot_idx on public.pokegang_saves(user_id, slot);

