-- ============================================================================
-- PvP gang competition: raid log and defender acknowledgements.
-- Raids no longer transfer reputation. `rep_delta` is retained as a legacy
-- audit field so older rows and app code can coexist during migration.
-- ============================================================================

create table if not exists public.pokegang_gang_raids (
  id                uuid        primary key default gen_random_uuid(),
  attacker_id       uuid        not null references auth.users(id) on delete cascade,
  defender_id       uuid        not null references auth.users(id) on delete cascade,
  attacker_gang     text        not null default 'Team ???',
  defender_gang     text        not null default 'Team ???',
  result            text        not null,
  rep_delta         integer     not null default 0, -- legacy audit field; PvP raids no longer change reputation
  money_penalty     integer     not null default 0,
  defender_snap_rep bigint      not null default 0,
  executed_at       timestamptz not null default now(),
  seen_by_defender  boolean     not null default false,
  constraint gang_raids_result_check check (result in ('attacker_win', 'defender_win'))
);

alter table public.pokegang_gang_raids add column if not exists attacker_id uuid references auth.users(id) on delete cascade;
alter table public.pokegang_gang_raids add column if not exists defender_id uuid references auth.users(id) on delete cascade;
alter table public.pokegang_gang_raids add column if not exists attacker_gang text not null default 'Team ???';
alter table public.pokegang_gang_raids add column if not exists defender_gang text not null default 'Team ???';
alter table public.pokegang_gang_raids add column if not exists result text not null default 'defender_win';
alter table public.pokegang_gang_raids add column if not exists rep_delta integer not null default 0;
alter table public.pokegang_gang_raids add column if not exists money_penalty integer not null default 0;
alter table public.pokegang_gang_raids add column if not exists defender_snap_rep bigint not null default 0;
alter table public.pokegang_gang_raids add column if not exists executed_at timestamptz not null default now();
alter table public.pokegang_gang_raids add column if not exists seen_by_defender boolean not null default false;

alter table public.pokegang_gang_raids enable row level security;

-- Defenders only need to acknowledge raids from the browser. Keep other raid
-- result columns immutable from the client after insertion.
revoke update on public.pokegang_gang_raids from anon, authenticated;
grant update (seen_by_defender) on public.pokegang_gang_raids to authenticated;

drop policy if exists "gang_raids_insert_attacker" on public.pokegang_gang_raids;
drop policy if exists "gang_raids_select_participant" on public.pokegang_gang_raids;
drop policy if exists "gang_raids_update_defender" on public.pokegang_gang_raids;

create policy "gang_raids_insert_attacker"
  on public.pokegang_gang_raids for insert
  to authenticated
  with check ((select auth.uid()) = attacker_id);

create policy "gang_raids_select_participant"
  on public.pokegang_gang_raids for select
  to authenticated
  using ((select auth.uid()) = attacker_id or (select auth.uid()) = defender_id);

create policy "gang_raids_update_defender"
  on public.pokegang_gang_raids for update
  to authenticated
  using ((select auth.uid()) = defender_id)
  with check ((select auth.uid()) = defender_id);

create index if not exists gang_raids_defender_pending_idx
  on public.pokegang_gang_raids(defender_id, seen_by_defender, executed_at desc);
create index if not exists gang_raids_attacker_idx
  on public.pokegang_gang_raids(attacker_id, executed_at desc);
create index if not exists gang_raids_defender_idx
  on public.pokegang_gang_raids(defender_id, executed_at desc);
